package org.mxhero.engine.plugin.attachmentlink.fileserver.exceptions;

public class NotAllowedToSeeContentException extends Exception {

	public NotAllowedToSeeContentException() {
		super();
	}

	public NotAllowedToSeeContentException(String message, Throwable cause) {
		super(message, cause);
	}

	public NotAllowedToSeeContentException(String message) {
		super(message);
	}

	public NotAllowedToSeeContentException(Throwable cause) {
		super(cause);
	}

}
